type MessageKey = keyof typeof messages["zh-CN"];

function normalizeLocale(locale?: string | null): AppLocale {
  if (!locale) return "zh-CN";
  return locale.toLowerCase().startsWith("en") ? "en-US" : "zh-CN";
}

export const appLocale = normalizeLocale(BUILD_LOCALE || (typeof navigator === "undefined" ? "zh-CN" : navigator.language));
export const isEnglish = appLocale === "en-US";

export function t(key: MessageKey, vars?: Record<string, string | number>) {
  const template = messages[appLocale][key];
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? `{${name}}`));
}

