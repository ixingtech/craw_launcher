export function translateBackendText(text?: string | null) {
  if (!text || !isEnglish) return text || "";
  const exact = backendExactMap[text];
  if (exact) return exact;
  for (const [pattern, formatter] of backendPatternMap) {
    const match = text.match(pattern);
    if (match) {
      return formatter(...match.slice(1));
    }
  }
  return text
    .replace(/(\d+) 字节/g, "$1 bytes")
    .replace(/^错误：/, "Error: ")
    .replace(/^间隔：/, "Recurring: ")
    .replace(/^单次：/, "One-time: ")
    .replace(/^状态：/, "Status: ");
}
